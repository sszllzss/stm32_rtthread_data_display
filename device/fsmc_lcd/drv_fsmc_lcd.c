//延时i
static void opt_delay(rt_uint8_t i)
{
	while(i--);
}

void fsmc_lcd_set_pixel(const char *color, int x, int y)
{
	LCD_Fast_DrawPoint(&_lcd,x,y,*(rt_uint16_t *)color);
}
void fsmc_lcd_get_pixel(char *color, int x, int y)
{
 	rt_uint16_t r=0,g=0,b=0;
	if(x>=_lcd.lcd_info.width||y>=_lcd.lcd_info.height){
		*(rt_uint16_t *)color = 0;
		return;	//超过了范围,直接返回 
	}
	LCD_SetCursor(&_lcd,x,y);	    
	if(_lcd.id==0X9341||_lcd.id==0X5310||_lcd.id==0X1963)LCD_WR_REG(0X2E);//9341/3510/1963 发送读GRAM指令
	else if(_lcd.id==0X5510)LCD_WR_REG(0X2E00);	//5510 发送读GRAM指令
 	r=LCD_RD_DATA();								//dummy Read	   
	if(_lcd.id==0X1963)
	{
		*(rt_uint16_t *)color = r;//1963直接读就可以 
		return;					
	}
	opt_delay(2);	  
 	r=LCD_RD_DATA();  		  						//实际坐标颜色
	//9341/NT35310/NT35510要分2次读出 
	opt_delay(2);	                                    
	b=LCD_RD_DATA(); 
	g=r&0XFF;		//对于9341/5310/5510,第一次读取的是RG的值,R在前,G在后,各占8位
	g<<=8; 
	*(rt_uint16_t *)color = (((r>>11)<<11)|((g>>10)<<5)|(b>>11));	//ILI9341/NT35310/NT35510需要公式转换一下
}

