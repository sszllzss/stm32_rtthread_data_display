int drv_fsmc_lcd_hw_init(void)
{
    rt_err_t result = RT_EOK;
    struct rt_device *device = &_lcd.parent;

    /* memset _lcd to zero */
    memset(&_lcd, 0x00, sizeof(_lcd));

    /* init lcd_lock semaphore */
    result = rt_sem_init(&_lcd.lcd_lock, "lcd_lock", 0, RT_IPC_FLAG_FIFO);
    if (result != RT_EOK)
    {
        LOG_E("init semaphore failed!\n");
        result = -RT_ENOMEM;
        goto __exit;
    }

    /* config LCD dev info */
    // _lcd.lcd_info.height = LCD_HEIGHT;
    // _lcd.lcd_info.width = LCD_WIDTH;
    _lcd.lcd_info.bits_per_pixel = LCD_BITS_PER_PIXEL;
    _lcd.lcd_info.pixel_format = LCD_PIXEL_FORMAT;

    device->type    = RT_Device_Class_Graphic;
#ifdef RT_USING_DEVICE_OPS
    device->ops     = &lcd_ops;
#else
    device->init    = drv_lcd_init;
	device->open    = drv_lcd_open;
	device->close   = drv_lcd_close;
    device->control = drv_lcd_control;
#endif

	device->user_data = &graphic_ops;

    /* init stm32 FSMC Lcd */
    if (stm32_fsmc_lcd_init(&_lcd) != RT_EOK)
    {
        result = -RT_ERROR;
        goto __exit;
    }
    else
    {
        turn_on_lcd_backlight();
    }

    /* register lcd device */
    result = rt_device_register(device, "lcd", RT_DEVICE_FLAG_RDWR);
    if (result != RT_EOK)
    {
        result = -RT_ERROR;
		LCD_DisplayOff(&_lcd);
		turn_off_lcd_backlight();
        goto __exit;
    }
__exit:
    if (result != RT_EOK)
    {
        rt_sem_detach(&_lcd.lcd_lock);
    }
    return result;
}
INIT_DEVICE_EXPORT(drv_fsmc_lcd_hw_init);

